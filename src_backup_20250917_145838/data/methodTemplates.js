// Integration in Session-Vorbereitung
export const getMethodTemplate = (methodKey, isCustom = false, customMethods = []) => {
  if (isCustom) {
    return customMethods.find(m => m.id === methodKey);
  }
  return methodTemplates[methodKey];
};

// Template-Platzhalter ersetzen
export const populateTemplate = (template, sessionData) => {
  return template
    .replace(/{{coachee_name}}/g, sessionData.coacheeName || '[Coachee Name]')
    .replace(/{{date}}/g, sessionData.date || new Date().toLocaleDateString('de-DE'))
    .replace(/{{duration}}/g, sessionData.duration || '60 Min')
    .replace(/{{method_title}}/g, sessionData.methodTitle || 'Coaching Session');
};